    private String ProjNamn(String projektId){
        String projektNamn=" ";
        try{
            String sqlFraga = "SELECT projektnamn FROM projekt where pid = '" + projektId + "'";
        projektNamn = idb.fetchSingle(sqlFraga);
        } catch (InfException ex){
           System.out.println(ex.getMessage());
        }

        return projektNamn;
    }

    
    
