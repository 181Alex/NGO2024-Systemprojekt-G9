    public int hogstaAid(InfDB idb){
        int hogsta=0;
        String sqlFraga="Select MAX(aid) FROM anstalld";
        try{
            String max=idb.fetchSingle(sqlFraga);
            hogsta=Integer.parseInt(max);
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        System.out.println(hogsta);
        return hogsta+1;
    }
    
